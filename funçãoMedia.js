function calculeMedia(n1, n2, n3){
    let media = (n1 + n2 + n3) / 3
    return "Sua média é", media
}

//Exemplo de uso 1 
let notasLucas = calculeMedia(8, 8.5, 7)
console.log("A nota do Lucas é", notasLucas)

//2
function calculeMedia2(n1, n2, n3, n4, n5){
    let media2 = (n1 + n2 + n3 + n4 + n5) / 5
    return "Sua média é", media2
}

let notasLucas2 = calculeMedia2(8, 8.5, 7, 6, 6)
console.log("A nota do Lucas é", notasLucas2)

